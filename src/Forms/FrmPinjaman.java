private void TambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TambahActionPerformed
// TODO add your handling code here:
    try {        
        
            Connection c=ClassDatabase.getkoneksi();
            Statement s= c.createStatement();
            String sql="Select NoPinjam from Pinjaman order by NoPinjam desc";
            ResultSet r=s.executeQuery(sql);
            
            if(r.next())
            {
                StringBuffer strBuf = new StringBuffer();
                strBuf.append(r.getString("NoPinjam"));
                String kd=String.valueOf(strBuf.delete(0, 2));
                int no_t=Integer.parseInt(kd) +1;
                
               
                this.TxtKodePinjam.setText("PJ" + String.valueOf(no_t));
            }else{
                int no_t=1;
                this.TxtKodePinjam.setText("PJ" + String.valueOf(no_t));
            }
            r.close();
            s.close();
        }catch(SQLException e) {
            System.out.println(e);
        }    
    //untuk tobol reset
        TxtJumlahPinjaman.setText("");
        TxtLamaPinjam.setText("");
        TxtAngsuran.setText("");
        TxtJasa.setText("");
        TxtBunga.setText("");
        
       
}//GEN-LAST:event_TambahActionPerformed

