private void TxtKodePinjamCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_TxtKodePinjamCaretUpdate
// TODO add your handling code here:
    try {            
            Connection c=ClassDatabase.getkoneksi();
            Statement s= c.createStatement();
            String sql="Select * from pinjaman where NoPinjam='" + this.TxtKodePinjam.getText() + "'";
            ResultSet r=s.executeQuery(sql);
            
           while (r.next()){
               
                
                this.TxtNoAnggota.setSelectedItem(r.getString("NoAnggota"));
                this.TxtLamaPinjam.setText(r.getString("Lama"));
                this.TxtBunga.setText(r.getString("Bunga"));
                this.TxtJumlahPinjaman.setText(r.getString("Jumlah"));
                this.TxtJasa.setText(r.getString("Jasa"));
                this.TxtAngsuran.setText(r.getString("Angsuran"));  
              
            }
            r.close();
            s.close();
            
        }catch(SQLException e) {
            System.out.println("Terjadi kesalahan" + e);
        }
}//GEN-LAST:event_TxtKodePinjamCaretUpdate

