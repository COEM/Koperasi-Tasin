    public void datatabel(){
        DefaultTableModel tab=new DefaultTableModel();
        tab.addColumn("Kode Pinjaman");
        tab.addColumn("Tanggal Pinjaman");
        tab.addColumn("No Anggota");
        tab.addColumn("Lama");
        tab.addColumn("Bunga");
        tab.addColumn("Jumlah");
        tab.addColumn("Jasa");
        tab.addColumn("Angsuran");
//        tab.addColumn("Total Angsuran");
        tab.addColumn("Sisa");
        
            Tabel3.setModel(tab);
            try{
                Statement statement=(Statement) ClassDatabase.getkoneksi().createStatement();
                ResultSet rs=statement.executeQuery("select distinct(Date_format(TglPinjam,'%d-%m-%Y')) as tgl,`NoPinjam`,`NoAnggota`,`Lama`,`Bunga`,`Jumlah`,`Jasa`,`Angsuran`,`Sisa` from pinjaman order by NoPinjam asc ");
                
                while(rs.next())
                {
                    tab.addRow(new Object[]{
                        rs.getString("NoPinjam"),
                        rs.getString("tgl"),
                        rs.getString("NoAnggota"),
                        rs.getString("Lama"),
                        rs.getString("Bunga"),
                        rs.getString("Jumlah"),
                        rs.getString("Jasa"),
                        rs.getString("Angsuran"),
//                        rs.getString("Total_Angsuran"),
                        rs.getString("Sisa")});
                        
                }
                Tabel3.setModel(tab);
                } catch (SQLException e){
            }
    }
