private void TxtNoAnggotaCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_TxtNoAnggotaCaretUpdate
// TODO add your handling code here:
    try {            
            Connection c=ClassDatabase.getkoneksi();
            Statement s= c.createStatement();
            String sql="Select * from anggota where NoAnggota='" + this.TxtNoAnggota.getText() + "'";
            ResultSet r=s.executeQuery(sql);
            
           while (r.next()){
                this.TxtNamaAnggota.setText(r.getString(2));
                this.TxtAlamat.setText(r.getString("Alamat"));
                this.TxtTgl.setDate(r.getDate("Tgl_Masuk"));
                this.TxtTempatLahir.setText(r.getString("TempatLahir"));
                this.TxtTglLahir.setDate(r.getDate("TglLahir"));
                this.TxtJenisKelamin.setSelectedItem(r.getString("JenisKelamin"));
                this.TxtSimpananPokok.setText(r.getString("SimpananPokok"));
                
            }
            r.close();
            s.close();
            
        }catch(SQLException e) {
            System.out.println("Terjadi kesalahan" + e);
        }
}//GEN-LAST:event_TxtNoAnggotaCaretUpdate

