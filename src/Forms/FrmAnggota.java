private void TambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TambahActionPerformed
// TODO add your handling code here:
   //untuk menambah data
    try {        
        
            Connection c=ClassDatabase.getkoneksi();
            Statement s= c.createStatement();
            String sql="Select NoAnggota from Anggota order by NoAnggota desc ";
            ResultSet r=s.executeQuery(sql);
            
            if(r.next())
            {
                StringBuffer strBuf = new StringBuffer();
                strBuf.append(r.getString("NoAnggota"));
                String kd=String.valueOf(strBuf.delete(0, 2));
                int no_t=Integer.parseInt(kd) +1;
                
               
                this.TxtNoAnggota.setText("AG" + String.valueOf(no_t));
            }else{
                int no_t=1;
                this.TxtNoAnggota.setText("AG" + String.valueOf(no_t));
            }
            r.close();
            s.close();
        }catch(SQLException e) {
            System.out.println(e);
        }        
     //untuk tobol reset
        TxtNamaAnggota.setText("");
        TxtAlamat.setText("");
        TxtTempatLahir.setText("");
        TxtTglLahir.setDate(null);
        TxtSimpananPokok.setText("");
        TxtTgl.setDate(null);
    
}//GEN-LAST:event_TambahActionPerformed

private void SimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SimpanActionPerformed
// TODO add your handling code here:
    //untuk menyimpan data
    java.util.Date tgl=(java.util.Date) this.TxtTgl.getDate();
    String kode=this.TxtNoAnggota.getText();
    String nama=this.TxtNamaAnggota.getText();
    String alamat=this.TxtAlamat.getText();
    String sp=this.TxtSimpananPokok.getText();
    String tl=this.TxtTempatLahir.getText();
    java.util.Date tgll=(java.util.Date) this.TxtTglLahir.getDate();
    String jenis=(String)this.TxtJenisKelamin.getSelectedItem();
    
    try {
            Connection c=ClassDatabase.getkoneksi();
            String sql = "Insert into anggota values (?,?,?,?,?,?,?,?)";
            PreparedStatement p=(PreparedStatement) c.prepareStatement(sql);
            p.setString(1, kode);
            p.setString(2, nama);  
            p.setString(3, alamat);
            p.setDate(4, new java.sql.Date(tgl.getTime()));
            p.setString(5, sp);
            p.setString(6, tl);
            p.setDate(7, new java.sql.Date(tgll.getTime()));
            p.setString(8, jenis);
            p.executeUpdate();
            p.close();
            JOptionPane.showMessageDialog(this, "Sukses Tambah data");
        }catch(SQLException e){
            System.out.println(e);
        }finally{
            
            
        }
    datatabel();
}//GEN-LAST:event_SimpanActionPerformed

