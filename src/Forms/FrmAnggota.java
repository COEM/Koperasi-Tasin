    public void datatabel(){
        DefaultTableModel tab=new DefaultTableModel();
        tab.addColumn("No Anggota");
        tab.addColumn("Nama Anggota");
        tab.addColumn("Alamat");
        tab.addColumn("Tanggal Masuk");
        tab.addColumn("Simpanan Pokok");
        tab.addColumn("Tempat Lahir");
        tab.addColumn("Tanggal Lahir");
        tab.addColumn("Jenis Kelamin");
        
            Tabel1.setModel(tab);
            try{
                Statement statement=(Statement) ClassDatabase.getkoneksi().createStatement();
                ResultSet rs=statement.executeQuery("select * from anggota order by NoAnggota");
                while(rs.next())
                {
                    tab.addRow(new Object[]{
                        rs.getString("NoAnggota"),
                        rs.getString("Nama"),
                        rs.getString("Alamat"),
                        rs.getString("Tgl_Masuk"),
                        rs.getString("SimpananPokok"),
                        rs.getString("TempatLahir"),
                        rs.getString("TglLahir"),
                        rs.getString("Jeniskelamin")});
                        
                }
                Tabel1.setModel(tab);
                } catch (SQLException e){
            }
    }
